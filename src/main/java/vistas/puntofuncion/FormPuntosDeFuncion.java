    public void limpiar (){
        this.cmbComponente.setSelectedItem(0);
        this.valor.setText(null);
        this.EIComboBox.setSelectedItem(0);
        this.complejidad.setText(null);
    }
  
    
    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        
        if ( !"".equals(cmbComponente.getSelectedItem().toString()) && !"".equals(valor.getText()) && !"".equals(complejidad.getText()) ){
            
            jTextArea1.append(cmbComponente.getSelectedItem().toString() + ": " + valor.getText() + " x " + complejidad.getText() +"\n");
            
            String tipo = cmbComponente.getSelectedItem().toString();
            int v = Integer.parseInt(valor.getText());
            int comp = Integer.parseInt(complejidad.getText());
            
            Complejidad c = new Complejidad(tipo,v,comp);
            lista.add(c);
                      
            this.limpiar();
        }else{
            JOptionPane.showMessageDialog(null, "Por favor ingrese todos los valores", "Atenci√≥n", JOptionPane.INFORMATION_MESSAGE);
            this.limpiar();
        } 

      
    }//GEN-LAST:event_btnOKActionPerformed
   
