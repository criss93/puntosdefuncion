    public void CalcularResultado(){
     int multiplicacion;
        
       int val;
       int fact;
       multiplicacion = 0;
       cuSinAjustar = 0;
       for (int i=0; i < lista.size(); i++){
          
           
           val =lista.get(i).getValor(); 
           fact = lista.get(i).getFactor();
           
           multiplicacion = val * fact;
           
           jTextArea1.append("\n");
           jTextArea1.append(lista.get(i).getTipo() + ": " + val + " x " + fact + " = " + multiplicacion + "\n");
           
           cuSinAjustar = cuSinAjustar + multiplicacion;
       }
        jTextArea1.append("TOTAL: " + cuSinAjustar + "\n");
   }
