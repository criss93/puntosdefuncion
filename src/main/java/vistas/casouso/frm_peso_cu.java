    private void btnOKClasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKClasActionPerformed
        if ( !"".equals(cmbClases.getSelectedItem().toString()) && !"".equals(textClases.getText()) && !"".equals(txtClas.getText()) ){
            jTextArea1.append("CLASES DE ANÁLISIS"+ "\n");
            jTextArea1.append(cmbClases.getSelectedItem().toString() + ": " + textClases.getText() + " x " + txtClas.getText() +"\n");

            String tipo = cmbClases.getSelectedItem().toString();
            int v = Integer.parseInt(textClases.getText());
            int f = Integer.parseInt(txtClas.getText());

            Factor factor = new Factor (tipo,v,f);
            lista.add(factor);

            this.limpiar();
        }else{
            JOptionPane.showMessageDialog(null, "Por favor ingrese todos los valores", "Atención", JOptionPane.INFORMATION_MESSAGE);
            this.limpiar();
        }
    }//GEN-LAST:event_btnOKClasActionPerformed

