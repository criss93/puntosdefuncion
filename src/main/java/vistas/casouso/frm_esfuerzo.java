    public float calcularEsfuerzo (float UCP, int CF1, int CF2){
        //Calculo de CF
        int CF = CF1 + CF2;
        int aux=0;
        if (CF <= 2){
            aux=20;
        }
        if (CF==3 | CF==4){
            aux=28;
        }
        if(CF >= 5){
            aux=36;
        }
        this.txtCF.setText(Integer.toString(aux));
        return UCP * aux;//
    }

